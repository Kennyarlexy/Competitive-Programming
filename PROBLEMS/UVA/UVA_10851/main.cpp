#include <bits/stdc++.h>
#define ll long long
using namespace std;

string A[15];

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    ll T; cin >> T;
    for (ll t = 1; t <= T; t++) {
        for (ll i = 0; i < 10; i++) {
            cin >> A[i];
        }

        string msg;
        for (ll j = 1; j < A[1].length()-1; j++) {
            ll bin = 0;
            for (ll i = 1; i <= 8; i++) {
                if (A[i][j] == '\\') bin |= (1LL << i);
            }

            msg.push_back((bin >> 1));
        }
        cout << msg << "\n";
    }
    
    return 0;
}

