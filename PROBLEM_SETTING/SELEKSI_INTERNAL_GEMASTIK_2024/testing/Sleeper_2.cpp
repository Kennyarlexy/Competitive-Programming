#include <bits/stdc++.h>
#define ll long long
using namespace std;

ll A[100005];
ll B[105];

ll f(int i, int j) {
    ll X = A[j];
    if (i > 0) X -= A[i-1];
    return X;
}

int main() {
    int N;
    scanf("%d", &N);
    for (int i = 1; i <= N; i++) {
        ll hh, mm, ss;
        scanf("%lld:%lld:%lld", &hh, &mm, &ss);

        ll X = hh*3600 + mm*60 + ss;
        A[X]++;
        B[i] = X;
    }
    
    if (N < 3) {
        printf("24\n");
    } else {
        for (int i = 1; i <= B[N]; i++) {
            A[i] += A[i-1];
        }

        ll Y = 0;
        for (int i = B[N-2]; i >= 3600; i--) {
            // cout << "i = " << i << "\n";
            if (f(i-3599, i-1) < 3) {
                Y++;
                i -= 3599;
            }
        }

        ll ans = Y/2;
        printf("%lld", ans);
        if (Y % 2 == 1) printf(".5");

        printf("\n");
        // printf("%lld", Y);
    }
    
    return 0;
}

